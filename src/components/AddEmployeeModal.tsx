  const [formData, setFormData] = useState<Omit<Employee, 'id'>>({
    first_name: editEmployee?.first_name || '',
    last_name: editEmployee?.last_name || '',
    email: editEmployee?.email || '',
    phone: editEmployee?.phone || '',
    position: editEmployee?.position || '',
    department: editEmployee?.department || 'Engineering',
    salary: editEmployee?.salary || 0,
    hire_date: editEmployee?.hire_date || new Date().toISOString().split('T')[0],
    status: editEmployee?.status || 'active',
    location: editEmployee?.location || '',
    employee_id: editEmployee?.employee_id || `EMP${String(Date.now()).slice(-3)}`,
  });

  useEffect(()=>{
    setFormData({
      first_name: editEmployee?.first_name || '',
      last_name: editEmployee?.last_name || '',
      email: editEmployee?.email || '',
      phone: editEmployee?.phone || '',
      position: editEmployee?.position || '',
      department: editEmployee?.department || 'Engineering',
      salary: editEmployee?.salary || 0,
      hire_date: editEmployee?.hire_date || new Date().toISOString().split('T')[0],
      status: editEmployee?.status || 'active',
      location: editEmployee?.location || '',
      employee_id: editEmployee?.employee_id || `EMP${String(Date.now()).slice(-3)}`,
    })
  },[editEmployee?.employee_id])

  
  console.log("again: ",formData)
 
  console.log("te: ",editEmployee?.employee_id)
  
